export default function(color) {
  if (color === "white") return "#fff";
  if (color === "black") return "#19191b";
  if (color === "gray") return "#747478";
  if (color === "lightGray") return "#ebe8e8";
  if (color === "lightOrange") return "#fc9b04";
  if (color === "darkOrange") return "#f05327";
  if (color === "blue") return "#3869e8";
  if (color === "green") return "#b5e237";
  return "#19191b";
}
